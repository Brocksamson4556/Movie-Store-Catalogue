
CREATE TABLE DISTRIBUTOR (COMPANY_ID VARCHAR(15) PRIMARY KEY,
	COMPANY_NAME VARCHAR(30),
	SALE_TYPE VARCHAR(30),
	MOVIE_TYPE VARCHAR(20));
CREATE TABLE DVD (DVD_TITLE VARCHAR(30) PRIMARY KEY,
	DVD_ID VARCHAR(10),
    DSN VARCHAR(10),
	UNIQUE_MOVIE_ID VARCHAR(10),
	Movie_Duration DECIMAL(3,2),
	MOVIE_RATING INTEGER ,
	RENT_PRICE DECIMAL(3,2),
	PURCHASE_PRICE DECIMAL(4,2),
	LATE_FEE DECIMAL(3,2),
	DAMAGE_FEE  DECIMAL(3,2),
	TAXES DECIMAL(3,2),
	NUMBER_OF_COPIES_RENTED INTEGER,
	NUMBER_OF_COPIES_IN_RESERVE INTEGER,
	DISTRIBUTOR_NAME VARCHAR(30),
	MOVIE_WRAPPED VARCHAR(3)
    );

CREATE TABLE VHS (VHS_TITLE VARCHAR(30) PRIMARY KEY,
	VHS_ID VARCHAR(10),
    DISTRUBUTOR_SERIAL_NUMBER VARCHAR(10),
	UNIQUE_MOVIE_ID VARCHAR(11),
	Movie_Duration DECIMAL (3,2),
	MOVIE_RATING INTEGER,
	RENT_PRICE DECIMAL(3,2),
	PURCHASE_PRICE DECIMAL(4,2),
	LATE_FEE DECIMAL(3,2),
	DAMAGE_FEE  DECIMAL(3,2),
	TAXES DECIMAL(3,2),
	NUMBER_OF_COPIES_RENTED INTEGER,
	NUMBER_OF_COPIES_IN_RESERVE INTEGER,
	DISTRIBUTOR VARCHAR(30),
	VHS_WRAPPED CHAR(1)
    );
CREATE TABLE MOVIE_CAST (DVD_TITLE VARCHAR(30) PRIMARY KEY,
    UNIQUE_MOVIE_ID VARCHAR(11),
    ACTORS VARCHAR(255),
    ACTRESSES VARCHAR(255)
    );
CREATE TABLE CUSTOMER (CUSTOMER_ID VARCHAR(10) PRIMARY KEY,
	FIRST_NAME VARCHAR(30),
	LAST_NAME VARCHAR(30),
    STREET_ADDRESS VARCHAR(40),
    ZIPCODE VARCHAR(5),
	ACCOUNT_BALANCE DECIMAL (3,2),
	RENTAL_STATUS CHAR(1)
    );
CREATE TABLE CUSTOMER_TITLES_RENTED (CUSTOMER_ID VARCHAR(10) PRIMARY KEY,
	VHS CHAR(1),
	DVD CHAR(1),
    MOVIE_TITLE VARCHAR(255),
	RENTED_TITLES_1 VARCHAR(255),
    RENTED_TITLES_2 VARCHAR(255),
 
    RENTED_DATE DATE
    );
    
